package com.cointoss;



public class OneCoinToss {

    private int heads=0;
    private int tails=0;


    public int getHeads() {
        return heads;
    }

    public void setHeads(int heads) {
        this.heads = heads;
    }

    public int getTails() {
        return tails;
    }

    public void setTails(int tails) {
        this.tails = tails;
    }
}
